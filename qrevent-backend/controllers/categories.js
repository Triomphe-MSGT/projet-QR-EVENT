const Category = require("../models/category");

/**
 * üü¢ Obtenir toutes les cat√©gories
 * Accessible uniquement aux administrateurs
 */
const getAllCategories = async (req, res, next) => {
  try {
    const categories = await Category.find().populate("events", "title date");
    res.json(categories);
  } catch (error) {
    next(error);
  }
};

/**
 * üü¢ Obtenir une cat√©gorie par ID
 */
const getCategoryById = async (req, res, next) => {
  try {
    const category = await Category.findById(req.params.id).populate(
      "events",
      "title date"
    );

    if (!category) {
      return res.status(404).json({ error: "Cat√©gorie non trouv√©e" });
    }

    res.json(category);
  } catch (error) {
    next(error);
  }
};

/**
 * üü¢ Cr√©er une nouvelle cat√©gorie
 */
const createCategory = async (req, res, next) => {
  try {
    const { name, emoji, description } = req.body;

    // V√©rifie si la cat√©gorie existe d√©j√†
    const existingCategory = await Category.findOne({ name });
    if (existingCategory) {
      return res.status(400).json({ error: "Cette cat√©gorie existe d√©j√†." });
    }

    const newCategory = new Category({ name, emoji, description });
    const savedCategory = await newCategory.save();

    res.status(201).json(savedCategory);
  } catch (error) {
    next(error);
  }
};

/**
 * üü° Mettre √† jour une cat√©gorie
 */
const updateCategory = async (req, res, next) => {
  try {
    const { name, emoji, description } = req.body;

    const updatedCategory = await Category.findByIdAndUpdate(
      req.params.id,
      { name, emoji, description },
      { new: true, runValidators: true }
    );

    if (!updatedCategory) {
      return res.status(404).json({ error: "Cat√©gorie non trouv√©e" });
    }

    res.json(updatedCategory);
  } catch (error) {
    next(error);
  }
};

/**
 * üî¥ Supprimer une cat√©gorie
 */
const deleteCategory = async (req, res, next) => {
  try {
    const deletedCategory = await Category.findByIdAndDelete(req.params.id);

    if (!deletedCategory) {
      return res.status(404).json({ error: "Cat√©gorie non trouv√©e" });
    }

    res.status(204).end();
  } catch (error) {
    next(error);
  }
};

module.exports = {
  getAllCategories,
  getCategoryById,
  createCategory,
  updateCategory,
  deleteCategory,
};
